  arch arm: "-arm64"

  version "1.105.0-insider,1759294995079,6e60e679fc6b6a512edbed9a750d7c5811595861"
  sha256 arm:   "73226c687eeeeb6fdcf633cbe19cc6fa8f15b6710dfc163b714aeb221c63c278",
